import React, { useCallback, useEffect, useState } from "react";
import { BrowserRouter, Switch, Route, Link } from "react-router-dom";
import "antd/dist/antd.css";
import {  JsonRpcProvider, Web3Provider } from "@ethersproject/providers";
import "./App.css";
import { Row, Col, Button, Menu, Radio, Typography } from "antd";
import Web3Modal from "web3modal";
import WalletConnectProvider from "@walletconnect/web3-provider";
import { useUserAddress } from "eth-hooks";
import { useExchangePrice, useGasPrice, useUserProvider, useContractLoader, useBalance } from "./hooks";
import { Header, Account, Faucet, Ramp, Contract, GasGauge } from "./components";
import { formatEther } from "@ethersproject/units";
import { Hints, Erc20Sandbox } from "./views"
import { INFURA_ID } from "./constants";
const { Title } = Typography;

const DEBUG = true

// üõ∞ providers
if(DEBUG) console.log("üì° Connecting to Mainnet Ethereum");
//const mainnetProvider = getDefaultProvider("mainnet", { infura: INFURA_ID, etherscan: ETHERSCAN_KEY, quorum: 1 });
// const mainnetProvider = new InfuraProvider("mainnet",INFURA_ID);
const mainnetProvider = new JsonRpcProvider("https://mainnet.infura.io/v3/"+INFURA_ID)
// ( ‚ö†Ô∏è Getting "failed to meet quorum" errors? Check your INFURA_ID)

// üè† Your local provider is usually pointed at your local blockchain
const localProviderUrl = "http://localhost:8545"; // for xdai: https://dai.poa.network
// as you deploy to other networks you can set REACT_APP_PROVIDER=https://dai.poa.network in packages/react-app/.env
const localProviderUrlFromEnv = process.env.REACT_APP_PROVIDER ? process.env.REACT_APP_PROVIDER : localProviderUrl;
if(DEBUG) console.log("üè† Connecting to provider:", localProviderUrlFromEnv);
const localProvider = new JsonRpcProvider(localProviderUrlFromEnv);



function App(props) {
  const [injectedProvider, setInjectedProvider] = useState();
  /* üíµ this hook will get the price of ETH from ü¶Ñ Uniswap: */
  const price = useExchangePrice(mainnetProvider); //1 for xdai

  /* üî• this hook will get the price of Gas from ‚õΩÔ∏è EtherGasStation */
  const gasPrice = useGasPrice("fast"); //1000000000 for xdai

  // For more hooks, check out üîóeth-hooks at: https://www.npmjs.com/package/eth-hooks

  // Use your injected provider from ü¶ä Metamask or if you don't have it then instantly generate a üî• burner wallet.
  const userProvider = useUserProvider(injectedProvider, localProvider);
  const address = useUserAddress(userProvider);

  // üèó scaffold-eth is full of handy hooks like this one to get your balance:
  const yourLocalBalance = useBalance(localProvider, address);
  if(DEBUG) console.log("üíµ yourLocalBalance",yourLocalBalance?formatEther(yourLocalBalance):"...")

  // just plug in different üõ∞ providers to get your balance on different chains:
  const yourMainnetBalance = useBalance(mainnetProvider, address);
  if(DEBUG) console.log("üíµ yourMainnetBalance",yourMainnetBalance?formatEther(yourMainnetBalance):"...")

  // Load in your local üìù contract and read a value from it:
  const readContracts = useContractLoader(localProvider)
  if(DEBUG) console.log("üìù readContracts",readContracts)

  // If you want to make üîê write transactions to your contracts, use the userProvider:
  const writeContracts = useContractLoader(userProvider)
  if(DEBUG) console.log("üîê writeContracts",writeContracts)

  let contractOptions = writeContracts?Object.keys(writeContracts):[]

  const [selectedContract, setSelectedContract] = useState()


  const loadWeb3Modal = useCallback(async () => {
    const provider = await web3Modal.connect();
    setInjectedProvider(new Web3Provider(provider));
  }, [setInjectedProvider]);

  useEffect(() => {
    if (web3Modal.cachedProvider) {
      loadWeb3Modal();
    }
  }, [loadWeb3Modal]);

  const [route, setRoute] = useState();
  useEffect(() => {
    setRoute(window.location.pathname)
  }, [setRoute]);

  return (
    <div className="App">

      {/* ‚úèÔ∏è Edit the header and change the title to your project name */}
      <Header />

      <BrowserRouter>

        <Menu style={{ textAlign:"center" }} selectedKeys={[route]} mode="horizontal">
          <Menu.Item key="/">
            <Link onClick={()=>{setRoute("/")}} to="/">Hints</Link>
          </Menu.Item>
          <Menu.Item key="/erc20-sandbox">
            <Link onClick={()=>{setRoute("/erc20-sandbox")}} to="/erc20-sandbox">Example UI</Link>
          </Menu.Item>
          <Menu.Item key="/contract">
            <Link onClick={()=>{setRoute("/contract")}} to="/contract">Contract</Link>
          </Menu.Item>
        </Menu>

        <Switch>
        <Route exact path="/">
          <Hints
          />
        </Route>
          <Route exact path="/erc20-sandbox">
            <Erc20Sandbox
            address={address}
            localContracts={writeContracts}
            mainnetProvider={mainnetProvider}
            />
          </Route>
          <Route path="/contract">
            <Title level={4}>Select contract:</Title>
            <Radio.Group
                options={contractOptions}
                onChange={(e) => { setSelectedContract(e.target.value)}}
                value={selectedContract}
              />
              {selectedContract?
              <Contract
                name={selectedContract}
                signer={userProvider.getSigner()}
                provider={localProvider}
                address={address}
              />:null}
          </Route>
        </Switch>
      </BrowserRouter>


      {/* üë®‚Äçüíº Your account is in the top right with a wallet at connect options */}
      <div style={{ position: "fixed", textAlign: "right", right: 0, top: 0, padding: 10 }}>
         <Account
           address={address}
           localProvider={localProvider}
           userProvider={userProvider}
           mainnetProvider={mainnetProvider}
           price={price}
           web3Modal={web3Modal}
           loadWeb3Modal={loadWeb3Modal}
           logoutOfWeb3Modal={logoutOfWeb3Modal}
         />
      </div>

      {/* üó∫ Extra UI like gas price, eth price, faucet, and support: */}
       <div style={{ position: "fixed", textAlign: "left", left: 0, bottom: 20, padding: 10 }}>
         <Row align="middle" gutter={[4, 4]}>
           <Col span={8}>
             <Ramp price={price} address={address} />
           </Col>

           <Col span={8} style={{ textAlign: "center", opacity: 0.8 }}>
             <GasGauge gasPrice={gasPrice} />
           </Col>
           <Col span={8} style={{ textAlign: "center", opacity: 1 }}>
             <Button
               onClick={() => {
                 window.open("https://t.me/joinchat/KByvmRe5wkR-8F_zz6AjpA");
               }}
               size="large"
               shape="round"
             >
               <span style={{ marginRight: 8 }} role="img" aria-label="support">
                 üí¨
               </span>
               Support
             </Button>
           </Col>
         </Row>

         <Row align="middle" gutter={[4, 4]}>
           <Col span={24}>
             {

               /*  if the local provider has a signer, let's show the faucet:  */
               localProvider && localProvider.connection && localProvider.connection.url && localProvider.connection.url.indexOf("localhost")>=0 && !process.env.REACT_APP_PROVIDER && price > 1 ? (
                 <Faucet localProvider={localProvider} price={price} ensProvider={mainnetProvider}/>
               ) : (
                 ""
               )
             }
           </Col>
         </Row>
       </div>

    </div>
  );
}


/*
  Web3 modal helps us "connect" external wallets:
*/
const web3Modal = new Web3Modal({
  // network: "mainnet", // optional
  cacheProvider: true, // optional
  providerOptions: {
    walletconnect: {
      package: WalletConnectProvider, // required
      options: {
        infuraId: INFURA_ID,
      },
    },
  },
});

const logoutOfWeb3Modal = async () => {
  await web3Modal.clearCachedProvider();
  setTimeout(() => {
    window.location.reload();
  }, 1);
};

export default App;
